                    <p class="section-subheading text-muted" style="text-align:justify;">
                        The paradigm of the study of this research utilizes the IPO (Input Process Output) Model as shown. The model
                        is a presentation of components  that interconnect to make up the whole system . The three major parts of this model
                        are input, process and output. Inputs are the variables that exist and are required in order to begin the production of
                        the system. Process are the different events and tasks that are involved in the duration of the production of the 
                        system. Output is the end viable product that resulted from the two major components of input and process.
                    </p>
