                        The conceptual model is presented through the figure. This figure shows four entities involved which are the jobseeker, backer, admin
                        and ePondo Web-Application. The first major step in the system is for jobseekers to post their campaigns and services on the platform. 
                        The second step consists of backers able to view these campaigns and services. Backers may opt to donate to campaigns or avail the 
                        services offered by these jobseekers. Donations to campaigns will be processed by the system and once successful jobseekers will be 
                        able to request withdrawal of these donations. The system will also function as a tool to manage service orders as well as process 
                        online payment methods for the service orders. For completed and successful service orders, jobseeker will be able to request withdrawal
                        of these service order earnings and the commissioned rewards earnings. The admin will have all functionalities to manage processes 
                        performed in the system as required or necessary for day to day operations.
